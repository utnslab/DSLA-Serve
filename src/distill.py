def cosine_alpha_scheduler(initial_alpha, alpha_decay, total_steps, current_step):
    """Cosine decay for alpha."""
    final_alpha = min(0, initial_alpha * alpha_decay)
    cosine_decay = 0.5 * (1 + math.cos(math.pi * current_step / total_steps))
    return final_alpha + (initial_alpha - final_alpha) * cosine_decay

def step_beta_scheduler(initial_beta, beta_max, step_interval, total_steps, current_step):
    beta_step = 0.001
    beta = initial_beta + (current_step // step_interval) * beta_step
    return beta

